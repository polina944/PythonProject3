from django.contrib import admin
#Импортирует модуль admin из Django
#тот модуль предоставляет функционал административной панели Django
#Админка позволяет управлять данными в базе данных через веб-интерфейс
from board.models import *
#Импортирует все модели из модуля models.py приложения board
#* означает "импортировать всё"
#Альтернатива: можно импортировать конкретные модели:
#from board.models import User, Post, Category, Comment
admin.site.register(User)
#Регистрирует модель User в административной панели
#Теперь модель будет отображаться в админке по адресу /admin/
#Django создаст стандартный интерфейс для CRUD операций (создание, чтение, обновление, удаление)
admin.site.register(Post)
#Регистрирует модель Post в админке
#Объявления/посты будут доступны для управления
admin.site.register(Category)
#Регистрирует модель Category
#Категории будут видны в административной панели
admin.site.register(Comment)
#Регистрирует модель Comment
#Комментарии/отклики появятся в админке

